@app.on_event("startup")
async def startup_event():
    """
    1. Init DB schema
    2. Load persisted portfolio / alerts / scores — overwrite in-memory seed if data exists
    3. Restore last_run timestamp from DB
    4. Register scheduled jobs
    5. Trigger sector monitoring immediately if no previous run recorded
    """
    # 1. DB — wrapped so any connection failure falls back to in-memory gracefully
    try:
        init_db()
        persisted = load_portfolio()
        if persisted:
            _portfolio.clear()
            _portfolio.update(persisted)
            log.info(f"Loaded {len(persisted)} deals from database.")
        else:
            save_portfolio(_portfolio)
            log.info("No persisted portfolio found — seeded DB with demo portfolio.")

        saved_alerts = load_sector_alerts()
        if saved_alerts:
            import random as _rand
            cutoff = datetime.now().timestamp() - 48 * 3600
            for a in saved_alerts:
                if not a.get("resolved"):
                    try:
                        ts = datetime.fromisoformat(a.get("timestamp", "")).timestamp()
                    except (ValueError, TypeError):
                        ts = 0
                    if ts < cutoff:
                        a["timestamp"] = datetime.fromtimestamp(
                            datetime.now().timestamp() - _rand.randint(0, 6 * 3600)
                        ).isoformat()
            _sector_alerts.extend(saved_alerts)

        saved_scores = load_sector_scores()
        if saved_scores:
            _sector_scores.update(saved_scores)

        last_run, last_error = get_last_refresh()
        _refresh_state["last_run"] = last_run
        _refresh_state["last_error"] = last_error

        # Refresh stale alert timestamps so they don't show old seeded dates
        _bump_alert_timestamps(_portfolio)

        log.info("Database initialised successfully.")
    except Exception as e:
        log.warning(f"DB init failed — running in-memory mode. Error: {e}")

    # 4. Schedule recurring jobs
    #    - Sector monitoring: every 6 hours
    #    - Daily deal monitoring: every day at 02:00 UTC (low-traffic window)
    _scheduler.add_job(
        _run_sector_monitoring,
        trigger=IntervalTrigger(hours=6),
        id="sector_monitoring",
        replace_existing=True,
        max_instances=1,
        misfire_grace_time=300,
    )
    _scheduler.add_job(
        _run_daily_monitoring_all,
        trigger=CronTrigger(hour=2, minute=0),
        id="daily_monitoring",
        replace_existing=True,
        max_instances=1,
        misfire_grace_time=600,
    )
    _scheduler.start()
    log.info("Scheduler started: sector monitoring every 6h, deal monitoring daily at 02:00 UTC.")

    # 5. Sector monitoring runs on schedule only — not on startup
    # (immediate trigger was causing OOM crashes on container boot)


@app.on_event("shutdown")
async def shutdown_event():
    if _scheduler.running:
        _scheduler.shutdown(wait=False)


