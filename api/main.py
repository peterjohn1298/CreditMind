def _bump_alert_timestamps(portfolio_dict: dict, max_age_hours: int = 48) -> None:
    """Refresh timestamps on unresolved alerts older than max_age_hours so they appear current."""
    import random
    cutoff = datetime.now().timestamp() - max_age_hours * 3600
    for deal in portfolio_dict.values():
        alerts = deal.get("human_alerts", [])
        for alert in alerts:
            if alert.get("resolved"):
                continue
            ts_str = alert.get("timestamp", "")
            try:
                ts = datetime.fromisoformat(ts_str).timestamp() if ts_str else 0
            except ValueError:
                ts = 0
            if ts < cutoff:
                # Spread refreshed alerts across last 6 hours for realism
                offset_secs = random.randint(0, 6 * 3600)
                alert["timestamp"] = datetime.fromtimestamp(
                    datetime.now().timestamp() - offset_secs
                ).isoformat()


def _run_daily_monitoring_all():
    """
    Scheduled job: run DailyMonitoringOrchestrator for every watchlist/stressed deal,
    plus any current deal that has active alerts. Runs in background thread via APScheduler.
    """
    if _refresh_state["running"]:
        log.info("Daily monitoring skipped — sector monitoring already running.")
        return

    from core.orchestrator import DailyMonitoringOrchestrator
    orchestrator = DailyMonitoringOrchestrator()
    target_deals = [
        deal for deal in _portfolio.values()
        if deal.get("loan_status") in ("watchlist", "stressed", "WATCHLIST", "STRESSED")
        or len(deal.get("human_alerts") or []) > 0
    ]

    log.info(f"Daily monitoring: running on {len(target_deals)} priority deals.")

    def _monitor_deal(deal: dict):
        try:
            updated = orchestrator.run(copy.deepcopy(deal))
            deal_id = deal["deal_id"]
            # Stamp unresolved alerts with current time so they reflect this monitoring run
            for alert in updated.get("human_alerts", []):
                if not alert.get("resolved"):
                    alert["timestamp"] = datetime.now().isoformat()
            _portfolio[deal_id] = updated
            save_deal(deal_id, updated)
        except Exception as e:
            log.warning(f"Daily monitoring failed for {deal.get('company')}: {e}")

    with ThreadPoolExecutor(max_workers=3) as executor:
        list(executor.map(_monitor_deal, target_deals))

    log.info("Daily monitoring complete.")


def _do_underwrite(req_dict: dict) -> dict:
    from core.loan_types import normalize_loan_type
    canonical = normalize_loan_type(req_dict["loan_type"])
    prefilled = {}
    for attr in (
        "sector", "description", "jurisdiction", "purpose",
        "total_facility", "pricing_spread_bps", "oid_pct", "call_protection", "expected_close",
        "revenue_ltm", "ebitda_ltm", "adj_ebitda_ltm", "revenue_growth_pct",
        "capex", "fcf", "total_debt_proforma", "equity_contribution", "enterprise_value",
        "leverage_covenant", "icr_covenant", "min_liquidity",
        "customer_concentration_pct", "recurring_revenue_pct",
        "management_tenure_years", "backlog", "key_risks", "esg_flags", "notes",
    ):
        val = req_dict.get(attr)
        if val is not None:
            prefilled[attr] = val
    from core.orchestrator import run_full_underwriting
    credit_state = run_full_underwriting(
        company=req_dict["company"],
        ticker=req_dict["ticker"],
        loan_amount=req_dict["loan_amount"],
        loan_tenor=req_dict["loan_tenor"],
        loan_type=canonical,
        sponsor=req_dict.get("sponsor", ""),
        portfolio=_portfolio,
        prefilled_application=prefilled,
    )
    for field in ("project_type", "offtake_type", "total_project_capex", "equity_pct", "bridge_exit_type"):
        if req_dict.get(field):
            credit_state[field] = req_dict[field]
    deal_id = credit_state.get("deal_id", f"{req_dict['ticker']}_{int(datetime.now().timestamp())}")
    credit_state["deal_id"] = deal_id
    credit_state = record_initial_rating(credit_state)
    _portfolio[deal_id] = credit_state
    save_deal(deal_id, credit_state)
    return credit_state


