@app.get("/api/sector/heat-map")
def sector_heat_map():
    from datetime import timedelta

    # All unique mapped sector names that have deals
    sectors = sorted(set(
        _SECTOR_MAP.get(d.get("sector", ""), d.get("sector", ""))
        for d in _portfolio.values()
        if d.get("sector")
    ))

    today = datetime.now()

    # 30-day history — deterministic random walk anchored at today's real score
    time_series = []
    for i in range(29, -1, -1):
        date = (today - timedelta(days=i)).strftime("%Y-%m-%d")
        scores = {}
        for s in sectors:
            base  = _sector_scores.get(s, 35)
            # Deterministic noise: varies ±12 further back in time
            noise = ((hash(s + date) % 25) - 12) * (i / 30)
            scores[s] = max(10, min(95, round(base + noise)))
        time_series.append({"date": date, "scores": scores, "is_forecast": False})

    # 7-day forecast — slight continuation of current trend
    forecast = []
    for i in range(1, 8):
        date = (today + timedelta(days=i)).strftime("%Y-%m-%d")
        scores = {}
        for s in sectors:
            base  = _sector_scores.get(s, 35)
            noise = ((hash(s + date + "fcast") % 10) - 4)
            scores[s] = max(10, min(95, round(base + noise + i * 0.3)))
        forecast.append({"date": date, "scores": scores, "is_forecast": True})

    return {
        "sectors": sectors,
        "time_series": time_series,
        "forecast": forecast,
        "portfolio_overlays": [
            {
                "deal_id":   d.get("deal_id"),
                "company":   d.get("company"),
                "sector_id": _SECTOR_MAP.get(d.get("sector", ""), d.get("sector", "")),
            }
            for d in _portfolio.values()
        ],
        "last_updated": _refresh_state.get("last_run") or datetime.now().isoformat(),
    }


