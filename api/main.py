def _run_sector_monitoring():
    """Background task: run NewsIntelligence + EarlyWarning for every sector in portfolio."""
    from agents.news_intelligence import NewsIntelligenceAgent
    from agents.early_warning import EarlyWarningAgent
    from core.credit_state import add_alert

    _refresh_state["running"] = True
    _refresh_state["last_error"] = None

    try:
        # Group portfolio deals by sector
        sector_deals: dict[str, list] = {}
        for deal in _portfolio.values():
            raw_sector = deal.get("sector", "")
            mapped = _SECTOR_MAP.get(raw_sector, raw_sector)
            if mapped not in sector_deals:
                sector_deals[mapped] = []
            sector_deals[mapped].append(deal)

        new_sector_alerts = []

        def _monitor_one_sector(sector_name: str, deals: list) -> list:
            keywords = _SECTOR_KEYWORDS.get(sector_name, {}).get("keywords", [sector_name])
            sector_state = {
                "sector": sector_name,
                "keywords": keywords,
                "deals_in_sector": [
                    {
                        "deal_id":       d.get("deal_id"),
                        "company":       d.get("company"),
                        "internal_rating": d.get("internal_rating"),
                        "loan_amount":   d.get("loan_amount", 0),
                        "risk_score":    d.get("live_risk_score", d.get("risk_score", 50)),
                    }
                    for d in deals
                ],
                "human_alerts": [],
                "news_signals": [],
                "agent_log": [],
            }

            # Run news intel first, then early warning uses its output
            news_agent = NewsIntelligenceAgent()
            sector_state = news_agent.run_sector(sector_state)

            ew_agent = EarlyWarningAgent()
            sector_state = ew_agent.run_sector(sector_state)

            # Store real sector stress score + component breakdown
            _sector_scores[sector_name] = sector_state.get("sector_risk_score", 35)
            if sector_state.get("sector_stress_detail"):
                _sector_scores[f"{sector_name}__detail"] = sector_state["sector_stress_detail"]

            # Push real signals back to each individual deal in this sector
            sector_news   = sector_state.get("news_signals", [])[:5]
            sector_flags  = sector_state.get("early_warning_flags", [])
            sector_halerts = sector_state.get("human_alerts", [])

            for deal in deals:
                deal_id = deal.get("deal_id")
                if deal_id in _portfolio:
                    _portfolio[deal_id]["news_signals"]       = sector_news
                    _portfolio[deal_id]["early_warning_flags"] = sector_flags
                    for alert in sector_halerts:
                        _portfolio[deal_id]["human_alerts"].append({
                            **alert,
                            "alert_type": "sector",
                            "sector_id":  sector_name,
                        })

            # Tag alerts with sector metadata and unique IDs
            alerts = []
            for i, alert in enumerate(sector_halerts):
                alerts.append({
                    **alert,
                    "alert_id":   f"sector-{sector_name[:6].lower().replace(' ', '')}-{int(datetime.now().timestamp())}-{i}",
                    "sector_id":  sector_name,
                    "alert_type": "sector",
                    "company":    f"{sector_name} Sector",
                    "message":    alert.get("trigger", ""),
                })
            return alerts

        # Run all sectors in parallel
        with ThreadPoolExecutor(max_workers=6) as executor:
            futures = {
                executor.submit(_monitor_one_sector, sector, deals): sector
                for sector, deals in sector_deals.items()
                if deals  # only sectors that have deals
            }
            for future in as_completed(futures):
                try:
                    new_sector_alerts.extend(future.result())
                except Exception as e:
                    sector = futures[future]
                    new_sector_alerts.append({
                        "alert_id":   f"sector-error-{int(datetime.now().timestamp())}",
                        "sector_id":  sector,
                        "alert_type": "sector",
                        "company":    f"{sector} Sector",
                        "severity":   "LOW",
                        "message":    f"Monitoring check failed: {str(e)}",
                        "timestamp":  datetime.now().isoformat(),
                        "resolved":   False,
                    })

        # Replace sector alerts with fresh results
        _sector_alerts.clear()
        _sector_alerts.extend(new_sector_alerts)
        _refresh_state["last_run"] = datetime.now().isoformat()

        # Persist everything to DB
        save_portfolio(_portfolio)
        save_sector_alerts(_sector_alerts)
        save_sector_scores(_sector_scores)
        log_refresh()
        log.info(f"Sector monitoring complete — {len(new_sector_alerts)} alerts, persisted to DB.")

    except Exception as e:
        _refresh_state["last_error"] = str(e)
        log_refresh(error=str(e))
    finally:
        _refresh_state["running"] = False


