@app.patch("/api/closing-checklist/{deal_id}/cp")
def update_cp_status(deal_id: str, cp_index: int, status: str, notes: str = ""):
    """Mark a condition precedent as satisfied, waived, or pending."""
    try:
        credit_state = _get_deal(deal_id)
        checklist = credit_state.get("cp_checklist", [])
        if cp_index < 0 or cp_index >= len(checklist):
            raise HTTPException(status_code=400, detail="Invalid CP index.")
        if status not in ("satisfied", "waived", "pending", "blocked"):
            raise HTTPException(status_code=400, detail="Invalid status.")

        checklist[cp_index]["status"] = status
        checklist[cp_index]["notes"]  = notes

        satisfied = sum(1 for c in checklist if c["status"] in ("satisfied", "waived"))
        total     = len(checklist)
        readiness = round((satisfied / total) * 100) if total else 0

        credit_state["cp_checklist"]      = checklist
        credit_state["closing_readiness"] = readiness
        if credit_state.get("closing_output"):
            credit_state["closing_output"]["cps_satisfied"]  = satisfied
            credit_state["closing_output"]["cps_pending"]    = total - satisfied
            credit_state["closing_output"]["closing_readiness_score"] = readiness

        _portfolio[deal_id] = credit_state
        save_deal(deal_id, credit_state)
        return {"deal_id": deal_id, "cp_index": cp_index, "status": status, "readiness_score": readiness}
    except HTTPException:
        raise
    except Exception as e:
        raise HTTPException(status_code=500, detail={"error": str(e)})


